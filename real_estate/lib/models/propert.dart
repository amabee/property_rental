class Property {
  final String id;
  final String houseNo;
  final String category;
  final int price;
  final String description;
  final String image;
  final bool isAvailable;

  Property({
    required this.id,
    required this.houseNo,
    required this.category,
    required this.price,
    required this.description,
    required this.image,
    required this.isAvailable,
  });
}
